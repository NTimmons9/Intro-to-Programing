import random

num1 = random.randint(1,100)
num2 = random.randint(1,100)

print (str(num1) + "+" + str(num2) + "=" + str(int(num1) + int(num2)))
print (str(num1) + "-" + str(num2) + "=" + str(int(num1) - int(num2)))
print (str(num1) + "*" + str(num2) + "=" + str(int(num1) * int(num2)))
print (str(num1) + "/" + str(num2) + "=" + str(int(num1) / int(num2)))